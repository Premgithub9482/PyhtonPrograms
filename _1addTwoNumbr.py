# Python Program to add two numbers

# with pre-defiined variable
num1 = 20
num2 = 10
sum = num1 + num2
print('the sum of given two number is ',sum)

# With user input adding two int or float values

num1 = float(input('enter first numeric value(int or float): '))
num2 = float(input('enter second numeric vlaue(int or float): '))
sum = num1 + num2

print(f"the sum of given number is {sum}")