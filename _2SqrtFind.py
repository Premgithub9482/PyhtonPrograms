# Calculating Square root program

# Using math module
import math
num = int(input("enter a integer number here: "))
sq_root = math.sqrt(num)
print(f"the square root of the given number is {sq_root}")

# Usiing Exponential
num = int(input('enter a integer number here: '))
sq_root = num**(0.5)
print(f"the square root of the given number is {sq_root}")